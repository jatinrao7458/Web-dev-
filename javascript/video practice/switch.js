let day = "Wednesday";

switch (day) {
  case "Monday":
    console.log("Start of the week.");
    break;
  case "Wednesday":
    console.log("Mid-week.");
    break;
  case "Friday":
    console.log("End of the week.");
    break;
  default:
    console.log("Another day.");
}
